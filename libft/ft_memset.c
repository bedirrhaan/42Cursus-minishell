#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	unsigned char	*p;
	size_t			i;

	i = 0;
	p = (unsigned char *)s;
	if (!p)
		return (0);
	while (n > i)
	{
		p[i] = c;
		i++;
	}
	return (s);
}
